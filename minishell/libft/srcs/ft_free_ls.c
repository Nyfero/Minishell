#include "../inc/libft.h"

void	ft_free_ls(char **ls)
{
	int	i;
	int	j;

	i = 0;
	j = ft_lstrlen(ls);
	while (i < j)
	{
		free(ls[i]);
		i++;
	}
	free(ls);
	return ;
}
